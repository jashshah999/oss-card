function getTheme(themeName, overrides = {}) {
  const base = themes[themeName] || themes.dark;
  const t = { ...base };

  if (overrides.bg_color) t.bg = `#${overrides.bg_color}`;
  if (overrides.border_color) t.border = `#${overrides.border_color}`;
  if (overrides.title_color) t.title = `#${overrides.title_color}`;
  if (overrides.text_color) t.text = `#${overrides.text_color}`;
  if (overrides.accent_color) {
    t.accent = `#${overrides.accent_color}`;
    t.bar = `#${overrides.accent_color}`;
    t.badgeBorder = `#${overrides.accent_color}`;
    t.badgeText = `#${overrides.accent_color}`;
  }

  return t;
}

module.exports = { themes, getTheme };
